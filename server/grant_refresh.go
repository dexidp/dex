// NewRefreshTokenPolicy parses the refresh-token configuration into a rotation
// strategy. It is the config-reading adapter; the strategy object itself lives
// in the refresh package, independent of how its intervals are configured.
func NewRefreshTokenPolicy(logger *slog.Logger, rotation bool, validIfNotUsedFor, absoluteLifetime, reuseInterval string) (*tokens.RefreshStrategy, error) {
	var validDur, absoluteDur, reuseDur time.Duration
	var err error

	if validIfNotUsedFor != "" {
		validDur, err = time.ParseDuration(validIfNotUsedFor)
		if err != nil {
			return nil, fmt.Errorf("invalid config value %q for refresh token valid if not used for: %v", validIfNotUsedFor, err)
		}
		logger.Info("config refresh tokens", "valid_if_not_used_for", validIfNotUsedFor)
	}

	if absoluteLifetime != "" {
		absoluteDur, err = time.ParseDuration(absoluteLifetime)
		if err != nil {
			return nil, fmt.Errorf("invalid config value %q for refresh tokens absolute lifetime: %v", absoluteLifetime, err)
		}
		logger.Info("config refresh tokens", "absolute_lifetime", absoluteLifetime)
	}

	if reuseInterval != "" {
		reuseDur, err = time.ParseDuration(reuseInterval)
		if err != nil {
			return nil, fmt.Errorf("invalid config value %q for refresh tokens reuse interval: %v", reuseInterval, err)
		}
		logger.Info("config refresh tokens", "reuse_interval", reuseInterval)
	}

	rotate := !rotation
	logger.Info("config refresh tokens rotation", "enabled", rotate)
	return tokens.NewRefreshStrategy(rotate, absoluteDur, validDur, reuseDur, time.Now), nil
}

func contains(arr []string, item string) bool {
	for _, itemFromArray := range arr {
		if itemFromArray == item {
			return true
		}
	}
	return false
}

type refreshError struct {
	msg  string
	code int
	desc string
}

func (r *refreshError) Error() string {
	return fmt.Sprintf("refresh token error: status %d, %q %s", r.code, r.msg, r.desc)
}

func newInternalServerError() *refreshError {
	return &refreshError{msg: oauth2.InvalidRequest, desc: "", code: http.StatusInternalServerError}
}

func newUpstreamRefreshError() *refreshError {
	return &refreshError{msg: oauth2.InvalidGrant, desc: "Upstream identity provider refresh failed.", code: http.StatusBadGateway}
}

func newBadRequestError(desc string) *refreshError {
	return &refreshError{msg: oauth2.InvalidRequest, desc: desc, code: http.StatusBadRequest}
}

var (
	invalidErr = newBadRequestError("Refresh token is invalid or has already been claimed by another client.")
	expiredErr = newBadRequestError("Refresh token expired.")
)

func (s *Server) refreshTokenErrHelper(w http.ResponseWriter, err *refreshError) {
	s.tokenErrHelper(w, err.msg, err.desc, err.code)
}

func (s *Server) extractRefreshTokenFromRequest(r *http.Request) (*internal.RefreshToken, *refreshError) {
	code := r.PostFormValue("refresh_token")
	if code == "" {
		return nil, newBadRequestError("No refresh token is found in request.")
	}

	token := new(internal.RefreshToken)
	if err := internal.Unmarshal(code, token); err != nil {
		// For backward compatibility, assume the refresh_token is a raw refresh token ID
		// if it fails to decode.
		//
		// Because refresh_token values that aren't unmarshable were generated by servers
		// that don't have a Token value, we'll still reject any attempts to claim a
		// refresh_token twice.
		token = &internal.RefreshToken{RefreshId: code, Token: ""}
	}

	return token, nil
}

// getRefreshTokenFromStorage validates that the refresh token exists, belongs to
// the requesting client, has not been claimed twice, and has not expired. It
// returns the stored token. Connector resolution and connector-data lookup are
// the refresh grant's concern (see handleRefreshToken); introspection only needs
// this validation.
func (s *Server) getRefreshTokenFromStorage(ctx context.Context, clientID *string, token *internal.RefreshToken) (*storage.RefreshToken, *refreshError) {
	refresh, err := s.storage.GetRefresh(ctx, token.RefreshId)
	if err != nil {
		if err != storage.ErrNotFound {
			s.logger.ErrorContext(ctx, "failed to get refresh token", "err", err)
			return nil, newInternalServerError()
		}
		return nil, invalidErr
	}

	// Only check ClientID if it was provided;
	if clientID != nil && (refresh.ClientID != *clientID) {
		s.logger.ErrorContext(ctx, "trying to claim token for different client", "client_id", clientID, "refresh_client_id", refresh.ClientID)
		// According to https://datatracker.ietf.org/doc/html/rfc6749#section-5.2 Dex should respond with an
		//  invalid grant error if token has already been claimed by another client.
		return nil, &refreshError{msg: oauth2.InvalidGrant, desc: invalidErr.desc, code: http.StatusBadRequest}
	}

	if refresh.Token != token.Token {
		switch {
		case !s.refreshTokenPolicy.AllowedToReuse(refresh.LastUsed):
			fallthrough
		case refresh.ObsoleteToken != token.Token:
			fallthrough
		case refresh.ObsoleteToken == "":
			s.logger.ErrorContext(ctx, "refresh token claimed twice", "token_id", refresh.ID)
			return nil, invalidErr
		}
	}

	if s.refreshTokenPolicy.CompletelyExpired(refresh.CreatedAt) {
		s.logger.ErrorContext(ctx, "refresh token expired", "token_id", refresh.ID)
		return nil, expiredErr
	}

	if s.refreshTokenPolicy.ExpiredBecauseUnused(refresh.LastUsed) {
		s.logger.ErrorContext(ctx, "refresh token expired due to inactivity", "token_id", refresh.ID)
		return nil, expiredErr
	}

	return &refresh, nil
}

// lookupRefreshToken resolves a refresh token for the introspection handler. It
// reports an inactive token (unknown, revoked or expired) as (nil, nil) so the
// handler need not know the refresh grant's sentinel errors.
func (s *Server) lookupRefreshToken(ctx context.Context, token *internal.RefreshToken) (*storage.RefreshToken, error) {
	rt, err := s.getRefreshTokenFromStorage(ctx, nil, token)
	if err != nil {
		if errors.Is(err, invalidErr) || errors.Is(err, expiredErr) {
			return nil, nil
		}
		return nil, err
	}
	return rt, nil
}

// refreshConnectorData returns the connector data to hand to the upstream refresh:
// the token's own data for legacy tokens that still carry it, otherwise the value
// held on the user's offline session.
func (s *Server) refreshConnectorData(ctx context.Context, refresh *storage.RefreshToken) ([]byte, *refreshError) {
	if len(refresh.ConnectorData) > 0 {
		return refresh.ConnectorData, nil
	}

	session, err := s.storage.GetOfflineSessions(ctx, refresh.Claims.UserID, refresh.ConnectorID)
	if err != nil {
		if err != storage.ErrNotFound {
			s.logger.ErrorContext(ctx, "failed to get offline session", "err", err)
			return nil, newInternalServerError()
		}
		return nil, nil
	}

	return session.ConnectorData, nil
}

func (s *Server) getRefreshScopes(r *http.Request, refresh *storage.RefreshToken) ([]string, *refreshError) {
	// Per the OAuth2 spec, if the client has omitted the scopes, default to the original
	// authorized scopes.
	//
	// https://tools.ietf.org/html/rfc6749#section-6
	scope := r.PostFormValue("scope")

	if scope == "" {
		return refresh.Scopes, nil
	}

	requestedScopes := strings.Fields(scope)
	var unauthorizedScopes []string

	// Per the OAuth2 spec, if the client has omitted the scopes, default to the original
	// authorized scopes.
	//
	// https://tools.ietf.org/html/rfc6749#section-6
	for _, requestScope := range requestedScopes {
		if !contains(refresh.Scopes, requestScope) {
			unauthorizedScopes = append(unauthorizedScopes, requestScope)
		}
	}

	if len(unauthorizedScopes) > 0 {
		desc := fmt.Sprintf("Requested scopes contain unauthorized scope(s): %q.", unauthorizedScopes)
		return nil, newBadRequestError(desc)
	}

	return requestedScopes, nil
}

// refreshWithConnector re-reads the identity from the upstream connector when it
// supports refreshing. Callers pass the connector data and scopes resolved for
// this refresh so the connector can validate and update the session.
func (s *Server) refreshWithConnector(ctx context.Context, conn connectors.Connector, connectorData []byte, scopes []string, ident connector.Identity) (connector.Identity, *refreshError) {
	// Can the connector refresh the identity? If so, attempt to refresh the data
	// in the connector.
	//
	// TODO(ericchiang): We may want a strict mode where connectors that don't implement
	// this interface can't perform refreshing.
	refreshConn, ok := conn.Connector.(connector.RefreshConnector)
	if !ok {
		return ident, nil
	}

	// Set connector data to the one received from an offline session
	ident.ConnectorData = connectorData
	s.logger.Debug("connector data before refresh", "connector_data", ident.ConnectorData)

	newIdent, err := refreshConn.Refresh(ctx, tokens.ParseScopes(scopes), ident)
	if err != nil {
		s.logger.ErrorContext(ctx, "failed to refresh identity", "err", err)
		return ident, newUpstreamRefreshError()
	}

	return newIdent, nil
}

// handleRefreshToken handles a refresh token request https://tools.ietf.org/html/rfc6749#section-6
// this method is the entrypoint for refresh tokens handling
func (s *Server) handleRefreshToken(w http.ResponseWriter, r *http.Request, client storage.Client) {
	token, rerr := s.extractRefreshTokenFromRequest(r)
	if rerr != nil {
		s.refreshTokenErrHelper(w, rerr)
		return
	}

	refresh, rerr := s.getRefreshTokenFromStorage(r.Context(), &client.ID, token)
	if rerr != nil {
		s.refreshTokenErrHelper(w, rerr)
		return
	}

	// The connector may have been removed from the client's allowed list after
	// this refresh token was issued; re-check on every refresh so a tightened
	// policy takes effect.
	if !s.checkConnectorAllowed(w, r, client, refresh.ConnectorID) {
		return
	}

	// Resolve the connector and confirm it still permits the refresh grant.
	conn, err := s.connectors.Get(r.Context(), refresh.ConnectorID)
	if err != nil {
		s.logger.ErrorContext(r.Context(), "connector not found", "connector_id", refresh.ConnectorID, "err", err)
		s.refreshTokenErrHelper(w, newInternalServerError())
		return
	}
	if !GrantTypeAllowed(conn.GrantTypes, oauth2.GrantTypeRefreshToken) {
		s.logger.ErrorContext(r.Context(), "connector does not allow refresh token grant", "connector_id", refresh.ConnectorID)
		s.refreshTokenErrHelper(w, &refreshError{msg: oauth2.InvalidRequest, desc: "Connector does not support refresh tokens.", code: http.StatusBadRequest})
		return
	}

	scopes, rerr := s.getRefreshScopes(r, refresh)
	if rerr != nil {
		s.refreshTokenErrHelper(w, rerr)
		return
	}

	var userIdent *storage.UserIdentity

	if s.sessionConfig != nil {
		ui, err := s.storage.GetUserIdentity(r.Context(), refresh.Claims.UserID, refresh.ConnectorID)
		if err != nil {
			s.logger.ErrorContext(r.Context(), "failed to get user identity", "err", err)
			s.refreshTokenErrHelper(w, newInternalServerError())
			return
		}
		userIdent = &ui
	}

	authTime := time.Time{}
	if userIdent != nil {
		authTime = userIdent.LastLogin
	}

	// When sessions are enabled, downstream refresh is disconnected from the upstream
	// identity provider: use the claims cached in UserIdentity at the last interactive
	// login instead of contacting the connector (which may fail if the upstream token
	// has expired). This matches other brokers (Keycloak, Auth0) that treat downstream
	// sessions independently from the upstream provider session lifetime. Connector data
	// is only needed on the connector-refresh path.
	freshIdentity := func(ctx context.Context) (connector.Identity, error) {
		if userIdent != nil {
			return tokens.IdentityFromClaims(userIdent.Claims), nil
		}
		connectorData, rerr := s.refreshConnectorData(ctx, refresh)
		if rerr != nil {
			return connector.Identity{}, rerr
		}
		ident, rerr := s.refreshWithConnector(ctx, conn, connectorData, scopes, tokens.IdentityFromClaims(refresh.Claims))
		if rerr != nil {
			return ident, rerr
		}
		return ident, nil
	}

	rawNewToken, ident, err := s.issuer.Refresh.Rotate(r.Context(), refresh, token, s.refreshTokenPolicy, freshIdentity)
	if err != nil {
		s.logger.ErrorContext(r.Context(), "failed to rotate refresh token", "err", err)
		s.refreshTokenErrHelper(w, newInternalServerError())
		return
	}

	auth := tokens.Authorization{
		Client: client,
		Claims: storage.Claims{
			UserID:            ident.UserID,
			Username:          ident.Username,
			PreferredUsername: ident.PreferredUsername,
			Email:             ident.Email,
			EmailVerified:     ident.EmailVerified,
			Groups:            ident.Groups,
		},
		Scopes:      scopes,
		ConnectorID: refresh.ConnectorID,
		Nonce:       refresh.Nonce,
		AuthTime:    authTime,
	}

	accessToken, _, err := s.issuer.SignAccessToken(r.Context(), auth)
	if err != nil {
		s.logger.ErrorContext(r.Context(), "failed to create new access token", "err", err)
		s.refreshTokenErrHelper(w, newInternalServerError())
		return
	}

	idToken, expiry, err := s.issuer.SignIDToken(r.Context(), auth, accessToken, "")
	if err != nil {
		s.logger.ErrorContext(r.Context(), "failed to create ID token", "err", err)
		s.refreshTokenErrHelper(w, newInternalServerError())
		return
	}

	ts := tokens.TokenSet{AccessToken: accessToken, IDToken: idToken, RefreshToken: rawNewToken, Expiry: expiry}
	if err := writeTokenResponse(w, ts, s.now()); err != nil {
		s.logger.ErrorContext(r.Context(), "failed to write token response", "err", err)
		s.refreshTokenErrHelper(w, newInternalServerError())
		return
	}
}
